    private void jbtOKActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jbtOKActionPerformed
    {//GEN-HEADEREND:event_jbtOKActionPerformed
        if (_addTeam)
        {

            if (_team.getName().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Empty team name.", "Empty field", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (_team.getPlayers().size() < 11)
            {
                JOptionPane.showMessageDialog(this, "Your team have less han 11 players ", "Wrong players numbers", JOptionPane.ERROR_MESSAGE);
                return;
            }

            for (int i = 0; i < 16; i++)
            {
                mPlayer player = _team.getPlayer(i + 1);
                if (player != null)
                {
                    if (player.getName().equals(""))
                    {
                        JOptionPane.showMessageDialog(this, "One of your player is unnamed", "Wrong player name", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
            }

            int result = JOptionPane.showConfirmDialog(this, "You will add your team. Do tou want to continue", "", JOptionPane.OK_CANCEL_OPTION);
            if (result == JOptionPane.OK_OPTION)
            {
                this.setVisible(false);
                mBBoS.getSingleton().addTeam(_team);
            }
        }
        else
        {
            setVisible(false);
        }
    }//GEN-LAST:event_jbtOKActionPerformed
